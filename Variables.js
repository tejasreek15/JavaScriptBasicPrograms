// Variables are of 3 types in JS. let, var, const.


// let variable example. you can change the value
let x = 2;
console.log(x)

x = 6
console.log(x)

let userName = "Honey"
console.log(userName)



// const variable example. once const as declared, you cannot change the value
let radius = 8
const pi = 3.14
let area

area = pi * radius * radius

console.log(area)